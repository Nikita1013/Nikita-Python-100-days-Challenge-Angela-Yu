# List====>       fruits = [item1, item2]

# simple list
fruits = ["apple", "cherry", "berry"]
print(fruits)

# append()
fruits.append("banana")
print(fruits)

# extend()
fruits.extend(["Chickoo","Guava"])
print(fruits)

# insert
fruits.insert(1,"plum")
print(fruits)

# pop()
fruits.pop(1)  # It will pop item at the index 1
print(fruits)

# remove()
fruits.remove("banana")
print(fruits)

# sort()
fruits.sort()
print(fruits)

# pop()
print(fruits.pop()) # It will pop the items from the end of the list